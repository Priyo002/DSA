void solve() {
	int n;
	cin >> n;
	vector<int> arr(n);

	vector<int> a(n), b(n);
	for (int i = 0; i < n; i++) cin >> a[i];
	for (int i = 0; i < n; i++) cin >> b[i];

	for (int i = 0; i < n; i++) {
		arr[i] = b[i] - a[i];
	}
	sort(arr);

	int ans = 0;

	int i = 0, j = n - 1;
	while (i < j) {
		int s = arr[i] + arr[j];
		if (s >= 0) {
			ans++;
			i++;
			j--;
		}
		else
			i++;
	}
	cout << ans << endl;
}

